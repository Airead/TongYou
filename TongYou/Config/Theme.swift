/// A terminal color theme defining palette colors 0-15 and semantic colors.
struct Theme: Equatable {
    /// The 16 ANSI palette colors (indices 0-15).
    let palette: [RGBColor]
    let foreground: RGBColor
    let background: RGBColor
    let cursorColor: RGBColor
    let cursorText: RGBColor
    let selectionBackground: RGBColor
    let selectionForeground: RGBColor

    init(
        palette: [RGBColor],
        foreground: RGBColor,
        background: RGBColor,
        cursorColor: RGBColor,
        cursorText: RGBColor,
        selectionBackground: RGBColor,
        selectionForeground: RGBColor
    ) {
        precondition(palette.count == 16, "Theme palette must have exactly 16 entries")
        self.palette = palette
        self.foreground = foreground
        self.background = background
        self.cursorColor = cursorColor
        self.cursorText = cursorText
        self.selectionBackground = selectionBackground
        self.selectionForeground = selectionForeground
    }
}

